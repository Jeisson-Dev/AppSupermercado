    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarActionPerformed
        // TODO add your handling code here:

        if (!txtcodigo.getText().isEmpty()) {
            if (!txtproducto.getText().isEmpty()) {
                if (!txtcantidad.getText().isEmpty()) {
                    if (!txtprecio.getText().isEmpty()) {
                        if (!txtconfiguracionaumento.getText().isEmpty()) {
                            try {

                                String[] agregar = new String[7];
                                agregar[0] = txtcodigo.getText();
                                agregar[1] = txtproducto.getText();
                                agregar[2] = txtcantidad.getText();
                                agregar[3] = txtprecio.getText();

                                agregar[5] = txtconfiguracionaumento.getText();
                                JOptionPane.showMessageDialog(this, " Se agrego el registro correctamente");

                                Double imp;

                                imp = Double.parseDouble(agregar[3]) * Double.parseDouble(agregar[2]);
                                imp = redondear(imp);
                                agregar[4] = imp.toString();

                                model.addRow(agregar);

                                txtcodigo.setText("");
                                txtproducto.setText("");
                                txtcantidad.setText("");
                                txtprecio.setText("");
                                txtconfiguracionaumento.setText("");

                            } catch (HeadlessException | NumberFormatException e) {
                                JOptionPane.showMessageDialog(this, e.getMessage());
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Si no desea aumentar el valor del producto a un % "
                                    + "ingresar 0/ presionar GUARDAR");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Por favor ingrese el precio del producto");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Por favor ingrese las unidades");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Por favor ingrese un producto");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un codigo");
        }


    }//GEN-LAST:event_BotonAgregarActionPerformed

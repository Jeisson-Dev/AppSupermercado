    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        double tot, igv, s = 0;
        double fin, totalapagar;

        for (int i = 0; i < Tabladedatos.getRowCount(); i++) {
            s = s + Double.parseDouble(model.getValueAt(i, 4).toString());
        }

        s = redondear(s);

        txtsubtotal.setText(String.valueOf(s));

        igv = 0.19 * s;
        igv = redondear(igv);
        tot = 1.19 * s;
        tot = redondear(tot);

        totalapagar = tot * aumento;
        fin = tot + totalapagar;

        txtiva.setText(String.valueOf(igv));
        totalneto.setText(String.valueOf("" + fin));


    }//GEN-LAST:event_jButton1ActionPerformed

